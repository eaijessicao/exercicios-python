#  usamos o parâmetro sep="_" para definir um _ entre cada valor, por isso é impresso. Definimos através do end para imprimir uma exclamação (!), seguida pelo \n. exemplo:

nome = "jessica"
sobrenome = "oliveira"
idade =  21 

print(nome, sobrenome, "tem", idade, sep="_", end="!\n")

